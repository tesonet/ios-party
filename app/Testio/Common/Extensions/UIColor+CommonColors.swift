import UIKit

extension UIColor {
    class var grayColor: UIColor {
        return UIColor.rgb(r: 153, g: 153, b: 153)
    }
    
    class var darkGrayColor: UIColor {
        return UIColor.rgb(r: 102, g: 102, b: 102)
    }
}
